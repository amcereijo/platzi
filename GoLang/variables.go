// Variables

package main

import "fmt"

func main() {

	var a string = "empezar"
	fmt.Println("a = ", a)

	var b, c, h int = 4, 5, 0
	fmt.Println("b, c, h", b, c, h)

	var i = 3
	fmt.Println("i = ", i)

	var d = true
	fmt.Println("d = ", d)

	var e int
	fmt.Println("e = ", e)

	var j bool
	fmt.Println("j = ", j)

	f:= "short"
	fmt.Println("f = ", f)

	k:= 4 + 6
	fmt.Println("k = ", k)
}
